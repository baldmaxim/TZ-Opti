/**
 * Идемпотентно заполняет таблицу characteristics стандартным шаблоном
 * для указанного тендера. Если уже есть хоть одна строка — ничего не делает.
 * Возвращает количество вставленных строк.
 */
async function populateStandardCharacteristics(tenderId) {
  const existing = await db.queryOne(
    'SELECT COUNT(*) AS c FROM characteristics WHERE tender_id = ?',
    tenderId,
  );
  if (existing && Number(existing.c) > 0) return 0;
  await db.transaction(async (tx) => {
    for (let i = 0; i < STANDARD_CHARACTERISTICS.length; i += 1) {
      await tx.queryRun(
        'INSERT INTO characteristics (id, tender_id, name, sort_order) VALUES (?, ?, ?, ?)',
        newId(), tenderId, STANDARD_CHARACTERISTICS[i], i + 1,
      );
    }
  });
  return STANDARD_CHARACTERISTICS.length;
}

